public class SalesItem {
    private string id;
    private int quantity;
    public void SalesItem(string id, int quantity) {
        id = id;
        quantity = quantity;
    }

    public string getId() {
        return id;
    }
    public void setId(string id) {
        id = id;
    }

    public int getQuantity() {
        return quantity;
    }
    public void setQuantity() {
        quantity=quantity;
    }
}
